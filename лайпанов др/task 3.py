# TODO  Напишите функцию count_letters
def count_letters(text):
    slovar = {}
    # создаем словарь, проходимся по каждому символу
    for i in text.lower():
        if i.isalpha() and slovar.get(i) == None:
            # если символ является буквой и он еще не добавлен в словарьб то создаем новое значение в словаре
            slovar[i] =  1
        elif i.isalpha():
            slovar[i] = slovar[i] + 1
    #     иначе прибавляем к уже существующему
    return slovar


# TODO Напишите функцию calculate_frequency
def calculate_frequency(slovar):
    summa = sum(slovar.values())
    #  суммируем все значения словарей, чтоб узнать, сколько всего
    for bykva in slovar.keys():
        slovar[bykva] = slovar[bykva] / summa
    #     для каждого ключа перезаписываем значение на частоту
    return slovar

